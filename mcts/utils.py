import random
import numpy as np


def rand_max(iterable, key=None):
    """
    Max function that tie breaks randomly instead of first-wins as with built-in function
    """
    if key is None:
        key = lambda x: x

    max_v = -np.inf
    max_l = []

    for item, value in zip(iterable, [key(i) for i in iterable]):
        if value == max_v:
            max_l.append(item)
        elif value > max_v:
            max_l = [item]
            max_v = value

    return random.choice(max_l)